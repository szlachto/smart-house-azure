package com.mycompany.app;

public class CmpVal {

	private float v;
	private int q;

	public CmpVal() {

	}

	public CmpVal(float v, int q) {
		setV(v);
		setQ(q);
	}

	public float getV() {
		return v;
	}

	public void setV(float v) {
		this.v = v;
	}

	public int getQ() {
		return q;
	}

	public void setQ(int q) {
		this.q = q;
	}

}
